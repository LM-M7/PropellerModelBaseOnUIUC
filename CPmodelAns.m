function CP = CPmodelAns(J, nps, beta)
% 此处模型为基于UIUC数据库得到的对于螺旋桨功率系数CP预测的通用模型，具体过程在MyModelCPTrain.m
% 同时，在使用时，应该注意不同与UIUC螺旋桨材料的螺旋桨并不符合本模型的使用，但相同材质的螺旋桨预测结果符合模型的拟合方程，可以自行整理数据集进行训练
a1 =  0.0097;    
a2 = -0.0282;    
a3 = -0.1213;    
a4 = -8.4817;    
a5 =  7.9859;    
a6 =  0.0421;    
a7 =  0.1732;   
a8 = -0.0491;   


CP = a1 + ...                 
    a2 * J + ...         
    a3 * J ^ 2 + ...        
    a4 * nps * J + ...  
    a5 * nps * J ^ 2 + ...  
    a6 * beta + ...       
    a7 * beta * J + ...   
    a8 * beta * J ^ 2;     
end